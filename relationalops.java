public class relationalops
 {
    public static void main(String[] args)
    {
        int x=23, y=32;
        System.out.println("x==y? "+(x==y));
        System.out.println("x!=y? "+(x!=y));
        System.out.println("x>y? "+(x>y));
        System.out.println("x>=y? "+(x>=y));
        System.out.println("x<y? "+(x<y));
        System.out.println("x<=y? "+(x<=y));
    }
}
